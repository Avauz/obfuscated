local function parseAnimalName(text)
	local cleaned = text
	
	local matches = {}
	for match in string.gmatch(cleaned, ">([^<]+)</") do
		if match and match ~= "" and not tonumber(match) then
			table.insert(matches, match)
		end
	end
	
	if #matches > 0 then
		return table.concat(matches, " ")
	end
	
	cleaned = string.gsub(cleaned, "<[^>]+>", "")
	
	local quoted = string.match(cleaned, "\"(.-)\"")
	if quoted and quoted ~= "" then
		return quoted
	end
	
	return cleaned
end

local function extractStealInfo(descendant)
	local itemName = nil
	
	local mainText = descendant.Text
	if string.find(mainText, "<") and string.find(mainText, ">") then
		local parsed = parseAnimalName(mainText)
		if parsed and parsed ~= "" and not string.find(parsed:lower(), "you stole") then
			itemName = parsed
		end
	end
	
	if descendant.Parent then
		for _, sibling in pairs(descendant.Parent:GetChildren()) do
			if sibling:IsA("TextLabel") and sibling ~= descendant then
				local text = sibling.Text
				
				if text and text ~= "" then
					if itemName == nil and not tonumber(text) and text:len() > 2 then
						if not string.find(text, "M") and not string.find(text, "K") and 
						   not string.find(text, "Generation") and not string.find(text, "Gen") and
						   not string.find(text, "N/A") and not string.find(text, "NOTIFICATION") and
						   not string.find(text, "Template") then
							if string.find(text, "<") and string.find(text, ">") then
								local parsed = parseAnimalName(text)
								if parsed and parsed ~= "" then
									itemName = parsed
								end
							else
								itemName = text
							end
						end
					end
				end
			end
		end
		
		if descendant.Parent.Parent and itemName == nil then
			for _, parentSibling in pairs(descendant.Parent.Parent:GetChildren()) do
				if parentSibling ~= descendant.Parent and (parentSibling:IsA("Frame") or parentSibling:IsA("TextLabel")) then
					for _, child in pairs(parentSibling:GetChildren()) do
						if child:IsA("TextLabel") then
							local text = child.Text
							if text and text ~= "" then
								if itemName == nil and not tonumber(text) and text:len() > 2 then
									if not string.find(text, "M") and not string.find(text, "K") and 
									   not string.find(text, "Generation") and not string.find(text, "Gen") and
									   not string.find(text, "N/A") and not string.find(text, "NOTIFICATION") and
									   not string.find(text, "Template") then
										if string.find(text, "<") and string.find(text, ">") then
											local parsed = parseAnimalName(text)
											if parsed and parsed ~= "" then
												itemName = parsed
											end
										else
											itemName = text
										end
									end
								end
							end
						end
					end
				end
			end
		end
	end
	
	return itemName or "Unknown Item"
end

local function setupStealDetection()
	local playerGui = player:WaitForChild("PlayerGui")
	
	local processingSteal = false
	local lastStealTime = 0
	
	playerGui.DescendantAdded:Connect(function(descendant)
		task.wait(0.15)
		
		if descendant:IsA("TextLabel") or descendant:IsA("TextBox") then
			local text = descendant.Text
			local lowerText = text:lower()
			
			if string.find(lowerText, "you stole") and 
			   not string.find(lowerText, "your") and
			   not string.find(lowerText, "stealing") and
			   not string.find(lowerText, "stolen") then
				
				local currentTime = tick()
				if currentTime - lastStealTime < 2 then
					return
				end
				lastStealTime = currentTime
				processingSteal = true
				
				local itemName = extractStealInfo(descendant)
				sendStealNotification(itemName)
				
				processingSteal = false
			end
		end
	end)
end

task.spawn(function()
	task.wait(1)
	setupStealDetection()
end)

